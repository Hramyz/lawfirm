const express = require('express');
const router = express.Router();
const pool = require('../db');

router.get('/', async (req, res) => {
  try {
    const result = await pool.query('SELECT * FROM payments ORDER BY payment_id');
    res.json(result.rows);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

router.get('/:id', async (req, res) => {
  try {
    const result = await pool.query('SELECT * FROM payments WHERE payment_id = $1', [req.params.id]);
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json(result.rows[0]);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

router.post('/', async (req, res) => {
  const { invoice_id, date_paid, amount, payment_method, notes } = req.body;
  try {
    const result = await pool.query(
      'INSERT INTO payments (invoice_id, date_paid, amount, payment_method, notes) VALUES ($1,$2,$3,$4,$5) RETURNING *',
      [invoice_id, date_paid, amount, payment_method, notes]
    );
    res.status(201).json(result.rows[0]);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

router.put('/:id', async (req, res) => {
  const { invoice_id, date_paid, amount, payment_method, notes } = req.body;
  try {
    const result = await pool.query(
      'UPDATE payments SET invoice_id=$1, date_paid=$2, amount=$3, payment_method=$4, notes=$5 WHERE payment_id=$6 RETURNING *',
      [invoice_id, date_paid, amount, payment_method, notes, req.params.id]
    );
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json(result.rows[0]);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

router.delete('/:id', async (req, res) => {
  try {
    const result = await pool.query('DELETE FROM payments WHERE payment_id=$1 RETURNING *', [req.params.id]);
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json({ message: 'Deleted successfully' });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

module.exports = router;
